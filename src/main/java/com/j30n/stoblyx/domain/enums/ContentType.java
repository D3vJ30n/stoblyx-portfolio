package com.j30n.stoblyx.domain.enums;

/**
 * 숏폼 콘텐츠의 유형을 나타내는 열거형
 */
public enum ContentType {
    /**
     * 비디오 형식의 콘텐츠
     */
    VIDEO,
    
    /**
     * 오디오 형식의 콘텐츠
     */
    AUDIO,
    
    /**
     * 이미지 형식의 콘텐츠
     */
    IMAGE,
    
    /**
     * 텍스트 형식의 콘텐츠
     */
    TEXT,
    
    /**
     * 혼합 형식의 콘텐츠 (여러 미디어 타입이 결합된 형태)
     */
    MIXED
} 